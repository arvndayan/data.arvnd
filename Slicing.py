#Slicing
# By using slicing you can return range of characters by using slice syntax
#by specifying the start index and end index, separated by colon to return a part of the string

b = "hello world"
print(b[2:5])
#here we are printing the range of characters we need ,this is called slicing

#slice from start

a = "Hai Aravind"
print(a[:5])
# this will start from first character

# slice to end

c= "welcome Aravind"
print(c[2:])
# it starts from second character and all the way to end

#Negative Indexing

z = "Good Morning"
print(z[-5:-2])

#slicing starts from the end of the string